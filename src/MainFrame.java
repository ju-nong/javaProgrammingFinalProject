    private void btnDeleteSubjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteSubjectActionPerformed
        // TODO add your handling code here:
        TimeTable tt = new TimeTable();
        TimeTableDAO ttDAO = new TimeTableDAO();
        int timeNo = -1;
        
        try {
            timeNo = Integer.parseInt(timeTableNo.getText());
            if(timeNo < 0) {
                JOptionPane.showMessageDialog(null, "수업을 선택해주세요.", "경고", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "수업을 선택해주세요.", "경고", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        tt.setTimeNo(timeNo);
        tt.setUserNo(loggedInNo);
        
        if(ttDAO.delete(tt)==1) {
            JOptionPane.showMessageDialog(null, "정보가 삭제되었습니다.");
            txtSubject.setText("");
            txtProf.setText("");
            txtClassroom.setText("");
            cbxDay.setSelectedIndex(0);
            cbxStartTime.setSelectedIndex(0);
            cbxEndTime.setSelectedIndex(0);
            loadTimeTable(ttDAO.getList(loggedInNo));
        } else {
            JOptionPane.showMessageDialog(null, "정보 삭제를 실패했습니다.", "경고", JOptionPane.ERROR_MESSAGE);
            return;
        }
        ttDAO.dbClose();
    }//GEN-LAST:event_btnDeleteSubjectActionPerformed


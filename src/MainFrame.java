    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        User user = new User();
        UserDAO userDAO = new UserDAO();
        
        String userId = txtRegId.getText();
        String userName = txtRegName.getText();
        String userPw = txtRegPw.getText();
        String userPwChk = txtRegPwChk.getText();
        
        if(!valueCheck(userId)) {
            JOptionPane.showMessageDialog(null, "아이디는 공백이거나 공백을 포함할 수 없습니다.", "경고", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(!overlapChk()) return;
        
        if(!valueCheck(userName)) {
            JOptionPane.showMessageDialog(null, "닉네임은 공백이거나 공백을 포함할 수 없습니다.", "경고", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(!valueCheck(userPw)) {
            JOptionPane.showMessageDialog(null, "비밀번호는 공백이거나 공백을 포함할 수 없습니다.", "경고", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        user.setUserId(userId);
        user.setUserName(userName);
        if(userPw.equals(userPwChk)) {
            user.setUserPw(userPw);
        } else {
            JOptionPane.showMessageDialog(null, "비밀번호가 일치하지 않습니다.", "경고", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if(userDAO.register(user) == 1) {
            JOptionPane.showMessageDialog(null, "회원가입 되었습니다.");
            jTabbedPane1.setSelectedIndex(0);
        } else {
            JOptionPane.showMessageDialog(null, "회원가입을 실패했습니다.", "경고", JOptionPane.ERROR_MESSAGE);
        }
        userDAO.dbClose();

    }//GEN-LAST:event_btnRegisterActionPerformed


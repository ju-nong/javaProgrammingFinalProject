    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String userId = txtLoginId.getText();
        String userPw = txtLoginPw.getText();
        
        UserDAO userDAO = new UserDAO();
        
        if(!valueCheck(userId)) {
            JOptionPane.showMessageDialog(null, "아이디를 입력해주세요.", "경고", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(!valueCheck(userPw)) {
            JOptionPane.showMessageDialog(null, "비밀번호를 입력해주세요.", "경고", JOptionPane.ERROR_MESSAGE);
            return;
        }
        User result = userDAO.login(userId, userPw);
        if(result != null) {
            JOptionPane.showMessageDialog(null, "로그인 성공.");
            loggedInNo = result.getUserNo();
            loggedInId = result.getUserId();
            loggedInName = result.getUserName();
            jTabbedPane1.addTab("시간표", jPanelTime);
            lblTimeTableName.setText(loggedInName+"님의 시간표");
            TimeTableDAO ttDAO = new TimeTableDAO();
            loadTimeTable(ttDAO.getList(loggedInNo));
            jTabbedPane1.setSelectedIndex(2);
           
        } else {
            JOptionPane.showMessageDialog(null, "아이디 또는 비밀번호가 일치하지 않습니다.", "경고", JOptionPane.ERROR_MESSAGE);
            return;
        }
        userDAO.dbClose();
    }//GEN-LAST:event_btnLoginActionPerformed

    private void btnAddSubjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddSubjectActionPerformed
        // TODO add your handling code here:
        TimeTable tt = new TimeTable();
        TimeTableDAO ttDAO = new TimeTableDAO();
        String subject = txtSubject.getText();
        String professor = txtProf.getText();
        String classroom = txtClassroom.getText();
        if(!valueCheck(subject) || !valueCheck(professor) || !valueCheck(classroom)) {
            JOptionPane.showMessageDialog(null, "빈 값은 입력할 수 없습니다.", "경고", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        tt.setSubject(subject);
        tt.setProfessor(professor);
        tt.setClassroom(classroom);
        tt.setDay(cbxDay.getSelectedIndex()+1);
        tt.setStartTime(cbxStartTime.getSelectedIndex());
        tt.setEndTime(cbxEndTime.getSelectedIndex());
        tt.setUserNo(loggedInNo);
       
        
        if(ttDAO.isOverlap(subject, loggedInNo) == 1) {
            JOptionPane.showMessageDialog(null, "이미 존재하는 교과목입니다.", "경고", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(ttDAO.addSubject(tt) == 1){
            loadTimeTable(ttDAO.getList(loggedInNo));
            JOptionPane.showMessageDialog(null, "수업을 추가했습니다.");
        } else {
            JOptionPane.showMessageDialog(null, "수업을 추가하는데 오류가 발생했습니다.", "경고", JOptionPane.ERROR_MESSAGE);
            return;
        }
        ttDAO.dbClose();
    }//GEN-LAST:event_btnAddSubjectActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        JTable source = (JTable)evt.getSource();
        int row = source.rowAtPoint(evt.getPoint());
        int column = source.columnAtPoint(evt.getPoint());
        String text = source.getModel().getValueAt(row,column)+"";
        System.out.println(text);
        if(column == 0) {
            return;
        }
        
        if(text.equals("null") || text.equals("")) {
            txtSubject.setText("");
            txtProf.setText("");
            txtClassroom.setText("");
            cbxDay.setSelectedIndex(0);
            cbxStartTime.setSelectedIndex(0);
            cbxEndTime.setSelectedIndex(0);
            timeTableNo.setText("-1");
            return;
        }
        String tmp = text.substring(6, text.length()-7);
        
        String[] arr = tmp.split("<br>");
        
        String subject = arr[0];
        String professor = arr[1];
        String classroom = arr[2];
        
        TimeTable tt = new TimeTable();
        tt.setSubject(subject);
        tt.setProfessor(professor);
        tt.setClassroom(classroom);
        
        TimeTable result = new TimeTableDAO().getOne(tt);
        
        txtSubject.setText(subject);
        txtProf.setText(professor);
        txtClassroom.setText(classroom);
        cbxDay.setSelectedIndex(result.getDay()-1);
        cbxStartTime.setSelectedIndex(result.getStartTime());
        cbxEndTime.setSelectedIndex(result.getEndTime());
        timeTableNo.setText(Integer.toString(result.getTimeNo()));
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnModifySubjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifySubjectActionPerformed
        // TODO add your handling code here:
        TimeTable tt = new TimeTable();
        TimeTableDAO ttDAO = new TimeTableDAO();
        String subject = txtSubject.getText();
        String professor = txtProf.getText();
        String classroom = txtClassroom.getText();
        int day = cbxDay.getSelectedIndex()+1;
        int startTime = cbxStartTime.getSelectedIndex();
        int endTime = cbxEndTime.getSelectedIndex();
        int timeNo = -1;
        
        try {
            timeNo = Integer.parseInt(timeTableNo.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "수업을 선택해주세요.", "경고", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        
        if(!valueCheck(subject) || !valueCheck(professor) || !valueCheck(classroom)) {
            JOptionPane.showMessageDialog(null, "빈 값 또는 공백은 입력할 수 없습니다.", "경고", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        
        tt.setSubject(subject);
        tt.setProfessor(professor);
        tt.setClassroom(classroom);
        tt.setDay(day);
        tt.setStartTime(startTime);
        tt.setEndTime(endTime);
        tt.setTimeNo(timeNo);
        tt.setUserNo(loggedInNo);
        
        if(ttDAO.isOverlap(subject, loggedInNo, timeNo) == 1) {
            JOptionPane.showMessageDialog(null, "이미 존재하는 교과목입니다.", "경고", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(ttDAO.update(tt)==1) {
            JOptionPane.showMessageDialog(null, "정보가 수정되었습니다.");
            loadTimeTable(ttDAO.getList(loggedInNo));
        } else {
            JOptionPane.showMessageDialog(null, "정보 수정을 실패했습니다.", "경고", JOptionPane.ERROR_MESSAGE);
            return;
        }
        ttDAO.dbClose();
    }//GEN-LAST:event_btnModifySubjectActionPerformed

